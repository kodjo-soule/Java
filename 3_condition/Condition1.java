import java.util.Scanner ;
public class Condition1{
    public static void main(String[] args){
        int nbre = -33 ;
        if (nbre > 0){
            System.out.println("Le nombre est positif") ;
        }
        else if (nbre == 0){
            System.out.println("Le nombre est null") ;
        }
        else{
            System.out.println("Le nombre est negatif") ;
        }
    }
}